class LocalFilePlayer {
    public void playLocalFile(String filename) {
        System.out.println("Playing local file: " + filename);
    }
}

class OnlineStreamPlayer {
    public void playStream(String url) {
        System.out.println("Streaming from URL: " + url);
    }
}

class RadioStationPlayer {
    public void playRadioStation(String station) {
        System.out.println("Tuning into radio station: " + station);
    }
}
